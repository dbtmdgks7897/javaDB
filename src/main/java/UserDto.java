public class UserDto {
    private String name;
    private int marble;
    private int id;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMarble() {
        return marble;
    }

    public void setMarble(int marble) {
        this.marble = marble;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
